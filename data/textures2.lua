
require('box')

box = make_box(2)

tex1 = color.texture('textures/box.png')
tex2 = color.texture('textures/box.png')
tex2:scale(2,2)
tex3 = color.texture('textures/box.png')
tex3:translate(0.5,0)
tex4 = color.texture('textures/box.png')
tex4:translate(0.5,0.5)
tex4:rotate(45)
tex4:translate(-0.5,-0.5)

mat1 = material.new()
mat1:set_diffuse(tex1)
mat2 = material.new()
mat2:set_diffuse(tex2)
mat3 = material.new()
mat3:set_diffuse(tex3)
mat4 = material.new()
mat4:set_diffuse(tex4)

b1 = scene.box('box', 0.8)
box:add_child(b1)
b1:set_material(mat1)
b1:translate({1,0.5,1})
b1:rotate('y', -20)
b1:rotate('x', -20)
b1:translate({-0.4,-0.4,-0.4})

b2 = scene.box('box', 0.8)
box:add_child(b2)
b2:set_material(mat2)
b2:translate({-1,0.5,1})
b2:rotate('y', 20)
b2:rotate('x', -20)
b2:translate({-0.4,-0.4,-0.4})

b3 = scene.box('box', 0.8)
box:add_child(b3)
b3:set_material(mat3)
b3:translate({1,-0.8,0})
b3:rotate('y', -10)
b3:rotate('x', -20)
b3:translate({-0.4,-0.4,-0.4})

b4 = scene.box('box', 0.8)
box:add_child(b4)
b4:set_material(mat4)
b4:translate({-1,-0.8,0})
b4:rotate('y', 10)
b4:rotate('x', -20)
b4:translate({-0.4,-0.4,-0.4})

do_render(box, 8, 's', 'textures2.png', 1000, 1000)
